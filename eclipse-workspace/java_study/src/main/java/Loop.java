
public class Ex18WhileLoop {
	public static void main(String[] args) {
		final int LOOP_COUNT=10;
		//1.loop 조건변수
		int i=0;
		while(i< LOOP_COUNT) {
			//2.반복코드
			System.out.println("hello");//10회 반복
			//3.조건변수 변경
			i = i+1;	
		}

	}
}
